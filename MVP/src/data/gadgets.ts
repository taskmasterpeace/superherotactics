// ==================== UTILITY FUNCTIONS ====================

export function getGadgetById(id: string): Gadget | undefined {
  return ALL_GADGETS.find((g) => g.id === id);
}

export function getGadgetByName(name: string): Gadget | undefined {
  return ALL_GADGETS.find((g) => g.name.toLowerCase() === name.toLowerCase());
}

export function getGadgetsByCategory(category: GadgetCategory): Gadget[] {
  return ALL_GADGETS.filter((g) => g.category === category);
}

export function getVehicleById(id: string): Vehicle | undefined {
  return ALL_VEHICLES.find((v) => v.id === id);
}

export function getVehicleByName(name: string): Vehicle | undefined {
  return ALL_VEHICLES.find((v) => v.name.toLowerCase() === name.toLowerCase());
}

export function getVehiclesByCategory(category: VehicleCategory): Vehicle[] {
  return ALL_VEHICLES.filter((v) => v.category === category);
}

export function getDroneById(id: string): Drone | undefined {
  return DRONES.find((d) => d.id === id);
}

export function getDroneByName(name: string): Drone | undefined {
  return DRONES.find((d) => d.name.toLowerCase() === name.toLowerCase());
}

export function getWeaponModById(id: string): WeaponMod | undefined {
  return WEAPON_MODS.find((m) => m.id === id);
}

/**
 * Get gadgets that can be used in combat (have AP cost > 0)
 */
export function getCombatGadgets(): Gadget[] {
  return ALL_GADGETS.filter((g) => g.apCost > 0);
}

/**
 * Get passive gadgets (always active)
 */
export function getPassiveGadgets(): Gadget[] {
  return ALL_GADGETS.filter((g) => g.operationType === 'passive');
}

/**
 * Get deployable gadgets
 */
export function getDeployableGadgets(): Gadget[] {
  return ALL_GADGETS.filter((g) => g.operationType === 'deploy');
}

/**
 * Get consumable gadgets
 */
export function getConsumableGadgets(): Gadget[] {
  return ALL_GADGETS.filter((g) => g.operationType === 'consumable');
}

/**
 * Get gadgets affordable within budget
 */
export function getAffordableGadgets(budget: number): Gadget[] {
  return ALL_GADGETS.filter((g) => g.costValue <= budget).sort(
    (a, b) => b.costValue - a.costValue
  );
}

/**
 * Get recommended gadgets for a skill set
 */
export function getRecommendedGadgets(
  budget: number,
  skills: string[]
): Gadget[] {
  return ALL_GADGETS.filter((g) => {
    if (g.costValue > budget) return false;
    if (g.skillRequired !== 'None' && !skills.includes(g.skillRequired))
      return false;
    return true;
  }).sort((a, b) => b.costValue - a.costValue);
}
