/**
 * Look up a country's LSW organization by ISO2 code OR country name (case-insensitive).
 * Note: 'CG' is shared by "Congo" and "Republic of the Congo" in the source data;
 * a code lookup returns the first (Congo). Use the full name to reach the second.
 */
export function getCountryOrganization(codeOrName: string): CountryOrganization | undefined {
  if (!codeOrName) return undefined;
  const q = codeOrName.trim().toLowerCase();
  return COUNTRY_ORGANIZATIONS.find(o => o.countryCode.toLowerCase() === q)
      ?? COUNTRY_ORGANIZATIONS.find(o => o.countryName.toLowerCase() === q);
}
